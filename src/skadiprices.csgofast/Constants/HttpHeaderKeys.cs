namespace skadiprices.csgofast.Constants
{
    /// <summary>
    /// Class to represent all HTTP header keys which are used.
    /// </summary>
    internal static class HttpHeaderKeys
    {
        /// <summary>
        /// Accept Header key in HTTP requests.
        /// </summary>
        internal const string Accept = "Accept";

        /// <summary>
        /// Accept-Encoding Header key in HTTP requests.
        /// </summary>
        internal const string AcceptEncoding = "Accept-Encoding";

        /// <summary>
        /// Accept-Language Header key in HTTP requests.
        /// </summary>
        internal const string AcceptLanguage = "Accept-Language";

        /// <summary>
        /// If-None-Match Header key in HTTP requests.
        /// </summary>
        internal const string IfNoneMatch = "If-None-Match";

        /// <summary>
        /// Origin Header key in HTTP requests.
        /// </summary>
        internal const string Origin = "Origin";

        /// <summary>
        /// User-Agent Header key in HTTP requests.
        /// </summary>
        internal const string UserAgent = "User-Agent";
    }
}
